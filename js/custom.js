document.addEventListener("DOMContentLoaded", function () {
  const titleElement = document.querySelector("#site-title");
  if (!titleElement) return; // 不是首页，直接退出

const poems = [
    "孤舟蓑笠翁，独钓寒江雪。", // 柳宗元《江雪》（借孤境悟坚守，观世态品淡泊）
    "长风破浪会有时，直挂云帆济沧海。", // 李白《行路难》（困顿时悟理想，失意中见豁达）
    "人生若只如初见，何事秋风悲画扇。", // 纳兰性德《木兰花·拟古决绝词柬友》（悟人情变迁，叹初心难留）
    "世事一场大梦，人生几度秋凉？", // 苏轼《西江月·中秋和子由》（观世事如梦，悟人生短暂）
    "天生我材必有用，千金散尽还复来。", // 李白《将进酒》（悟自我价值，抒豁达胸襟）
    "谁道人生无再少？门前流水尚能西！休将白发唱黄鸡。", // 苏轼《浣溪沙·游蕲水清泉寺》（叹时光易逝，悟老当益壮）
    "无可奈何花落去，似曾相识燕归来。小园香径独徘徊。", // 晏殊《浣溪沙》（悟世事无常，品得失淡然）
    "曾经沧海难为水，除却巫山不是云。", // 元稹《离思五首·其四》（经世事沉淀，悟真情难得）
    "年年岁岁花相似，岁岁年年人不同。", // 刘希夷《代悲白头翁》（观花木依旧，悟人生易老）
    "悲欢离合总无情，一任阶前、点滴到天明。", // 蒋捷《虞美人·听雨》（历人生百态，悟心境自洽）
    "人生到处知何似，应似飞鸿踏雪泥。", // 苏轼《和子由渑池怀旧》（悟人生漂泊，惜过往痕迹）
    "莫言下岭便无难，赚得行人错喜欢。正入万山圈子里，一山放过一山拦。", // 杨万里《过松源晨炊漆公店》（悟人生坎坷，明前行多阻）
    "当时只道是寻常。", // 纳兰性德《浣溪沙·谁念西风独自凉》（忆往事如烟，悟寻常最珍贵）
    "万物静观皆自得，四时佳兴与人同。", // 程颢《秋日偶成》（观万物运转，悟心境与自然相融）
    "不是人间富贵花，却教风雨送年华。", // 纳兰性德《采桑子·塞上咏雪花》（悟自我本真，不逐世俗浮华）
    "人生忽如寄，寿无金石固。", // 无名氏《古诗十九首·驱车上东门》（叹人生短暂，悟珍惜当下）
    "旧游无处不堪寻，无寻处，惟有少年心。", // 章良能《小重山》（寻过往踪迹，悟少年心境难寻）
    "莫听穿林打叶声，何妨吟啸且徐行。竹杖芒鞋轻胜马，谁怕？一蓑烟雨任平生。", // 苏轼《定风波》（遇困境从容，悟豁达自适）
    "大都好物不坚牢，彩云易散琉璃脆。", // 白居易《简简吟》（观美好易逝，悟世事难久）
    "是非成败转头空，青山依旧在，几度夕阳红。", // 杨慎《临江仙·滚滚长江东逝水》（看历史沉浮，悟成败皆空）
	"浮生若梦，为欢几何？"
];

  let poemIndex = Math.floor(Math.random() * poems.length);
  let charIndex = 0;
  const speed = 120; // 打字速度
  const delay = 2000; // 一句结束后的停顿时间

  titleElement.textContent = "";

  function typeWriter() {
    if (charIndex < poems[poemIndex].length) {
      titleElement.textContent += poems[poemIndex].charAt(charIndex);
      charIndex++;
      setTimeout(typeWriter, speed);
    } else {
      setTimeout(() => {
        titleElement.textContent = "";
        charIndex = 0;
        poemIndex = (poemIndex + 1) % poems.length;
        typeWriter();
      }, delay);
    }
  }

  typeWriter();
});
